import os
import sys
from typing import List

# - good = works
#   - main = useful
#   - extra = not very useful
# - bad = doesn't work

EXTRA: List[str] = ['helloworld']
BAD: List[str] = []


def scan():
    all = set(f for f in os.listdir('.') if os.path.isdir(f))
    bad = set(BAD)
    good = all - bad
    extra = set(EXTRA)
    main = good - extra
    return locals()


plugins = scan()


def parse(argv):
    if len(argv) == 1:
        return plugins['all']
    return plugins[argv[1]]


if __name__ == '__main__':
    names = sorted(parse(sys.argv))
    print((' '.join(names)))
